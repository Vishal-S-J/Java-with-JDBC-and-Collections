package com.beanModel;

public class Student {
    private int S_ID, S_AGE;
    private String S_FNAME, S_LNAME, S_GENDER, S_BRANCH;

    public int getS_ID() {
        return S_ID;
    }

    public void setS_ID(int s_ID) {
        S_ID = s_ID;
    }

    public int getS_AGE() {
        return S_AGE;
    }

    public void setS_AGE(int s_AGE) {
        S_AGE = s_AGE;
    }

    public String getS_FNAME() {
        return S_FNAME;
    }

    public void setS_FNAME(String s_FNAME) {
        S_FNAME = s_FNAME;
    }

    public String getS_LNAME() {
        return S_LNAME;
    }

    public void setS_LNAME(String s_LNAME) {
        S_LNAME = s_LNAME;
    }

    public String getS_GENDER() {
        return S_GENDER;
    }

    public void setS_GENDER(String s_GENDER) {
        S_GENDER = s_GENDER;
    }

    public String getS_BRANCH() {
        return S_BRANCH;
    }

    public void setS_BRANCH(String s_BRANCH) {
        S_BRANCH = s_BRANCH;
    }
}
